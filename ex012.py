#Fórmula para calcular porcentagem valor +ou- (valor * 0/100)
preço = float(input ("Qual o preço do produto: R$"))
novo = preço - (preço * 5/100)
print (f"O produto que custava R${preço:.2f}, com um desconto de 5%, vai custa {novo:.2f}")